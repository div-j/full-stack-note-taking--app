import mongoose from "mongoose";

const userSchema =  mongoose.Schema(
  {
    name: {
      type: String,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    image: {
      type: String,
    },
  },
  { timestamp: true }
);


export const User = mongoose.model("User", userSchema);

